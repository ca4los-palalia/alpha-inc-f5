package com.cplsystems.stock.app.vm.requisicion.utils;

public class CotizacionListaExcelFile {

	String no;
	String clave;
	String nombreProducto;
	String partidaGenericaNombre;
	String cantidad;
	String unidadMedida;
	String precioUnitario;
	String total;
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	public String getNombreProducto() {
		return nombreProducto;
	}
	public void setNombreProducto(String nombreProducto) {
		this.nombreProducto = nombreProducto;
	}
	public String getPartidaGenericaNombre() {
		return partidaGenericaNombre;
	}
	public void setPartidaGenericaNombre(String partidaGenericaNombre) {
		this.partidaGenericaNombre = partidaGenericaNombre;
	}
	public String getCantidad() {
		return cantidad;
	}
	public void setCantidad(String cantidad) {
		this.cantidad = cantidad;
	}
	public String getUnidadMedida() {
		return unidadMedida;
	}
	public void setUnidadMedida(String unidadMedida) {
		this.unidadMedida = unidadMedida;
	}
	public String getPrecioUnitario() {
		return precioUnitario;
	}
	public void setPrecioUnitario(String precioUnitario) {
		this.precioUnitario = precioUnitario;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	
	
}
